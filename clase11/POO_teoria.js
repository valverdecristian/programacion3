// POO (Programación Orientada a Objetos): Es un paradigma de programación
// que se enfoca en modelar objetos del mundo real a través de código. 
// Consiste en abstraer los elementos importantes de estos objetos y representarlos en el programa.

// MÉTODOS: Funcionalidades que puede ejecutar un objeto.
// PROPIEDADES: Características o atributos del objeto.
// CLASES: Plantillas o moldes para crear objetos. Encapsulan las propiedades 
// y métodos que serán utilizados en las instancias de los objetos.

/* PILARES DE LA POO
    >> ABSTRACCION: es eliminar los detalles innecesarios.
    >> ENCAPSULAMIENTO: ocultar detalles (del funcionamiento) que son irrelevantes para el exterior.
    >> HERENCIA: forma en la que una clase (hija) hereda de otra clase (padre) sus metodos y atributos.
    >> POLIMORFISMO: capacidad de realizar una misma accion (metodo) con un resultado diferente.
*/


/* MODIFICADORES DE ACCESO
    Utilizamos estos modificadores para especificar si queremos que el acceso de una propiedad
    o metodo sea publico o privado.

    >> public | se puede acceder desde cualquier parte del codigo.
    >> private | solo es accesible desde dentro de la clase que lo define.
*/

/*  GETTER |
        Un getter es un metodo que permite acceder a una propiedad privada
        de un objeto en lugar de usar la propiedad directamente
    
    SETTER |
        Un setter es un metodo que permite acceder y modificar una propiedad
        privada de un objeto en lugar de modificar la propiedad directamente.
*/